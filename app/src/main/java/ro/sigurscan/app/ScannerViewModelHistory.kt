// Scan history persistence, extracted from ScannerViewModel.

internal fun ScannerViewModel.addToHistory(item: OfflineAssessment) {
    historyItems.add(0, item)
    calculateStats()
    saveHistory()
}

fun ScannerViewModel.deleteHistoryItem(item: OfflineAssessment) {
    historyItems.remove(item)
    saveHistory()
}

fun ScannerViewModel.clearHistory() {
    historyItems.clear()
    saveHistory()
}

internal fun ScannerViewModel.saveHistory() {
    val snapshot = historyItems.toList().take(50)
    viewModelScope.launch(Dispatchers.IO) {
        val json = gson.toJson(snapshot)
        prefs.edit().putString("history", json).apply()
    }
}

internal fun ScannerViewModel.loadHistory() {
    val json = prefs.getString("history", null)
    if (json != null) {
        val type = object : TypeToken<List<OfflineAssessment>>() {}.type
        val items: List<OfflineAssessment> = gson.fromJson(json, type)
        historyItems.clear()
        historyItems.addAll(items)
    }
}
